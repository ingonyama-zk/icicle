template <const uint8_t k, const uint8_t du, const uint8_t dv, const int batch_size = 1>
__global__ void test_pke_decrypt_kernel(
  const uint8_t dk_pke[384 * k * batch_size],
  const uint8_t c[32 * (du * k + dv) * batch_size],
  uint8_t m[32 * batch_size])
{
  if (blockIdx.x >= batch_size) { return; }
  dk_pke += blockIdx.x * 384 * k;
  c += blockIdx.x * 32 * (du * k + dv);
  m += blockIdx.x * 32;

  pke::decrypt<k, du, dv>(dk_pke, c, m);
}

class PkeDecryptTest : public IcicleTestBase
{
public:
  static const std::string TEST_DATA_PATH;
  static const size_t BATCH_SIZE;

  void SetUp() override
  {
    IcicleTestBase::SetUp();
    std::cout << "\nRunning tests with batch size: " << BATCH_SIZE << std::endl;
  }

  template <typename T>
  static void randomize(T* arr, uint64_t size)
  {
    // Fill the array with random values
    uint32_t* u32_arr = (uint32_t*)arr;
    for (int i = 0; i < (size * sizeof(T) / sizeof(uint32_t)); ++i) {
      u32_arr[i] = rand_uint_32b();
    }
  }

  static std::string voidPtrToHexString(const std::byte* byteData, size_t size)
  {
    std::ostringstream hexStream;
    for (size_t i = 0; i < size; ++i) {
      hexStream << std::hex << std::setw(2) << std::setfill('0') << static_cast<int>(byteData[i]);
    }
    return hexStream.str();
  }

  template <const uint8_t k, const uint8_t du, const uint8_t dv>
  void decrypt_test(const uint8_t* reference_dk_pke, const uint8_t* reference_c, const uint8_t* reference_m)
  {
    constexpr size_t c_size = 32 * (du * k + dv);

    uint8_t* d_c;
    uint8_t* d_m;
    uint8_t* d_dk_pke;

    cudaMalloc(&d_dk_pke, 384 * k * BATCH_SIZE);
    cudaMalloc(&d_c, c_size * BATCH_SIZE);
    cudaMalloc(&d_m, 32 * BATCH_SIZE);

    for (int i = 0; i < BATCH_SIZE; ++i) {
      cudaMemcpy(d_dk_pke + i * 384 * k, reference_dk_pke, 384 * k, cudaMemcpyHostToDevice);
      cudaMemcpy(d_c + i * c_size, reference_c, c_size, cudaMemcpyHostToDevice);
    }

    test_pke_decrypt_kernel<k, du, dv, BATCH_SIZE><<<BATCH_SIZE, 128>>>(d_dk_pke, d_c, d_m);

    cudaError_t err = cudaDeviceSynchronize();
    if (err != cudaSuccess) { printf("Kernel launch failed with error \"%s\"\n", cudaGetErrorString(err)); }

    uint8_t h_m[32];

    for (int i = 0; i < BATCH_SIZE; ++i) {
      cudaMemcpy(h_m, d_m + i * 32, 32, cudaMemcpyDeviceToHost);
      err = cudaDeviceSynchronize();
      if (err != cudaSuccess) { printf("Result copy failed with error \"%s\"\n", cudaGetErrorString(err)); }

      for (int j = 0; j < 32; j++) {
        if (h_m[j] != reference_m[j]) {
          EXPECT_EQ(h_m[j], reference_m[j]) << "Mismatch at i = " << i << " byte " << j;
          break;
        }
      }
    }

    cudaFree(d_c);
    cudaFree(d_m);
    cudaFree(d_dk_pke);
  }
};

