class MLKemEncapsTest : public IcicleTestBase
{
public:
  static const size_t BATCH_SIZE;

  void SetUp() override
  {
    IcicleTestBase::SetUp();
    std::cout << "\nRunning tests with batch size: " << BATCH_SIZE << std::endl;
  }

  template <typename T>
  static void randomize(T* arr, uint64_t size)
  {
    // Fill the array with random values
    uint32_t* u32_arr = (uint32_t*)arr;
    for (int i = 0; i < (size * sizeof(T) / sizeof(uint32_t)); ++i) {
      u32_arr[i] = rand_uint_32b();
    }
  }

  static std::string voidPtrToHexString(const std::byte* byteData, size_t size)
  {
    std::ostringstream hexStream;
    for (size_t i = 0; i < size; ++i) {
      hexStream << std::hex << std::setw(2) << std::setfill('0') << static_cast<int>(byteData[i]);
    }
    return hexStream.str();
  }

  template <const uint8_t k, const uint8_t eta1, const uint8_t eta2, const uint8_t du, const uint8_t dv>
  void encaps_test(
    const uint8_t* reference_ek, const uint8_t* reference_m, const uint8_t* reference_K, const uint8_t* reference_c)
  {
    const size_t ek_size = 384 * k + 32;
    const size_t c_size = 32 * (du * k + dv);
    const size_t m_size = 32;
    const size_t K_size = 32;

    uint8_t* d_ek;
    uint8_t* d_m;
    uint8_t* d_K;
    uint8_t* d_c;
    Zq* d_A;

    const int BATCH_SIZE = 1 << 13;

    // Allocate memory for inputs and outputs
    cudaMalloc(&d_ek, ek_size * BATCH_SIZE);
    cudaMalloc(&d_m, m_size * BATCH_SIZE);
    cudaMalloc(&d_K, K_size * BATCH_SIZE);
    cudaMalloc(&d_c, c_size * BATCH_SIZE);
    cudaMalloc(&d_A, PolyMatrix<256, k, k, Zq>::byte_size() * BATCH_SIZE);

    // Copy inputs from host to device
    for (int i = 0; i < BATCH_SIZE; ++i) {
      cudaMemcpy(d_ek + i * ek_size, reference_ek, ek_size, cudaMemcpyHostToDevice);
      cudaMemcpy(d_m + i * m_size, reference_m, m_size, cudaMemcpyHostToDevice);
    }

    cudaError_t err = cudaDeviceSynchronize();
    if (err != cudaSuccess) { printf("Copy failed with error \"%s\"", cudaGetErrorString(err)); }

    ml_kem_encaps_kernel<k, eta1, eta2, du, dv><<<BATCH_SIZE, 128>>>(d_ek, d_m, d_K, d_c, d_A);

    err = cudaDeviceSynchronize();
    if (err != cudaSuccess) { printf("Kernel launch failed with error \"%s\"", cudaGetErrorString(err)); }

    uint8_t h_K[K_size];
    uint8_t h_c[c_size];

    for (int i = 0; i < BATCH_SIZE; ++i) {
      cudaMemcpy(h_K, d_K + i * K_size, K_size, cudaMemcpyDeviceToHost);
      cudaMemcpy(h_c, d_c + i * c_size, c_size, cudaMemcpyDeviceToHost);
      err = cudaDeviceSynchronize();
      if (err != cudaSuccess) { printf("Copy result failed with error \"%s\"", cudaGetErrorString(err)); }
      for (int j = 0; j < K_size; ++j) {
        ASSERT_EQ(h_K[j], reference_K[j]) << "K check mismatch: i = " << i << " j = " << j;
      }
      for (int j = 0; j < c_size; ++j) {
        ASSERT_EQ(h_c[j], reference_c[j]) << "c check mismatch: i = " << i << " j = " << j;
      }
    }

    cudaFree(d_ek);
    cudaFree(d_m);
    cudaFree(d_K);
    cudaFree(d_c);
    cudaFree(d_A);
  }
};

